CREATE TABLE PUBLISHERS_2611
(
PUBID int PRIMARY KEY,
PNAME VARCHAR(30),
EMAIL VARCHAR(50) UNIQUE,
PHONE VARCHAR(30)
);

CREATE TABLE SUBJECTS_2611
(
SUBID VARCHAR(5) PRIMARY KEY,
SNAME VARCHAR(30)
);

CREATE TABLE AUTHORS_2611
(
AUID int PRIMARY KEY,
ANAME VARCHAR(30),
EMAIL VARCHAR(50) UNIQUE,
PHONE VARCHAR(30)
);

CREATE TABLE TITLES_2611
(
TITLEID int PRIMARY KEY,
TITLE VARCHAR(30),
PUBID int REFERENCES PUBLISHERS_2611 (PUBID),
SUBID VARCHAR(5) REFERENCES SUBJECTS_2611(SUBID),
PUBDATE DATE,
COVER CHAR(1) CHECK ( COVER IN ('P','H','p','h')),
PRICE int
);

CREATE TABLE TITLEAUTHORS_2611
(
TITLEID int REFERENCES TITLES_2611(TITLEID),
AUID int REFERENCES AUTHORS_2611(AUID),
IMPORTANCE int,
CONSTRAINT TITLESAUTHORS_PK PRIMARY KEY(TITLEID,AUID)
);

